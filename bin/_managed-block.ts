/**
 * Pure marker-fenced managed-block helpers — no I/O, fully unit-testable.
 * A "managed block" is a region of a text file Cortex owns and regenerates,
 * fenced by HTML-comment markers so the rest of the file is never touched.
 */
const open = (id: string) =>
  `<!-- cortex:managed:${id} — regenerated by Cortex; edit the source memory, not this block -->`
const close = (id: string) => `<!-- /cortex:managed:${id} -->`
// A loose open matcher (the human/older versions may have a different trailing note).
const openRe = (id: string) => new RegExp(`<!-- cortex:managed:${id}\\b[^>]*-->`)
const closeRe = (id: string) => new RegExp(`<!-- /cortex:managed:${id} -->`)

/** Inner content between the markers, or null if no well-formed block exists. */
export function extractManagedBlock(text: string, id: string): string | null {
  const o = text.match(openRe(id)); if (!o || o.index == null) return null
  const after = o.index + o[0].length
  const c = text.slice(after).match(closeRe(id)); if (!c || c.index == null) return null
  return text.slice(after, after + c.index).replace(/^\n+|\n+$/g, '')
}

/** Replace the block's inner lines, or append a new block if none well-formed.
 * Bytes outside the markers are preserved verbatim. Deterministic + idempotent. */
export function upsertManagedBlock(text: string, id: string, innerLines: string[]): string {
  const inner = innerLines.join('\n')
  const block = `${open(id)}\n${inner}\n${close(id)}`
  const o = text.match(openRe(id)); const c = text.match(closeRe(id))
  if (o && c && o.index != null && c.index != null && c.index > o.index) {
    return text.slice(0, o.index) + block + text.slice(c.index + c[0].length)
  }
  // No well-formed block (absent, or malformed/dangling) → append once, leave existing bytes intact.
  const base = text.replace(/\s+$/, '')
  return `${base}\n\n${block}\n`
}

/** Strip a well-formed block (markers + inner), preserving everything else. */
export function removeManagedBlock(text: string, id: string): string {
  const o = text.match(openRe(id)); const c = text.match(closeRe(id))
  if (!o || !c || o.index == null || c.index == null || c.index < o.index) return text
  const before = text.slice(0, o.index).replace(/\n+$/, '\n')
  const afterIdx = c.index + c[0].length
  const after = text.slice(afterIdx).replace(/^\n+/, '')
  return (before + after).replace(/\n{3,}/g, '\n\n')
}
