/**
 * Single source of truth for the Cortex statusline dot snippets — the exact shell
 * functions a user pastes into their own statusline (or that `ckn-statusline`
 * scaffolds into a minimal one). Verbatim from the tested reference; the doc
 * (`docs/statusline-bus-dot.md`) ships these same texts and a drift-guard test
 * asserts they match, so the two cannot diverge.
 *
 * Both dots are CHEAP-PER-RENDER — a statusline runs on every prompt, so neither
 * may spawn `tsx`/`ckn-bus`. The bus dot is a pure `/proc` scan; the mesh dot is a
 * single bounded local `curl` to the auth-free `/api/bus/mesh-status` diagnostic.
 */

/** Shared ANSI color defs the render blocks reference. */
export const COLOR_DEFS = `GREEN='\\033[32m'; YELLOW='\\033[33m'; RED='\\033[31m'; DIM='\\033[2m'; RST='\\033[0m'`

/**
 * Bus dot — returns 0 (armed) when a `ckn-bus watch` Monitor task is running for
 * $1 (the session id), else 1. Pure /proc scan; no network, no Cortex dependency.
 * Mirrors `watcherRunningForSession` in `bin/ckn-pause-context.ts`.
 */
export const BUS_DOT_FN = `bus_watcher_armed() {
  local sid="$1" pid cmd
  [ -z "$sid" ] && return 1
  for pid in /proc/[0-9]*; do
    cmd=$(tr '\\0' ' ' < "$pid/cmdline" 2>/dev/null) || continue
    case "$cmd" in
      *ckn-bus*watch*)
        case "$cmd" in *"$sid"*) return 0 ;; esac          # bound via --session arg
        if tr '\\0' '\\n' < "$pid/environ" 2>/dev/null | grep -q "^CLAUDE_CODE_SESSION_ID=$sid$"; then
          return 0                                         # or via environ
        fi
        ;;
    esac
  done
  return 1
}`

/**
 * Mesh dot — BINARY + link-aware (reflects the membership-tick OUTCOME, not just
 * `enabled`/armed; and "am I on the mesh", NOT a peer count). A NAT'd relay node
 * (WSL / a laptop on its own LAN) reaches the fleet by RELAYING through a reachable
 * hub, so a direct-dial reachable-count misleads (shows 1-of-N even with the fleet
 * up). Connected = the tier is live AND there is >=1 live LINK — a connected wsLink
 * OR a reachable canonical-http peer (the relay hub):
 *   green  ● mesh  = enabled AND live AND >=1 live link (connected, direct or relay)
 *   red    ● mesh… = enabled but no link yet (tick retrying: VPN/token not up)
 *   yellow ○ local = not enabled (running local-only by choice)
 *   dim    ○ mesh  = curl empty/fails (server unreachable — undetermined)
 * Needs the COLOR_DEFS vars in scope. One bounded (~9ms) local read; never a spawn.
 */
export const MESH_DOT_FN = `mesh_seg() {
  local j enabled live linked
  j=$(curl -s --max-time 1 http://localhost:3001/api/bus/mesh-status 2>/dev/null)
  [ -z "$j" ] && { printf '%s' "\${DIM}○ mesh\${RST}"; return; }
  enabled=$(printf '%s' "$j" | jq -r '.enabled // false')
  live=$(printf '%s' "$j" | jq -r '.live // false')
  linked=$(printf '%s' "$j" | jq -r '[(.wsLinks[]?|select(.connected==true)), (.peers[]?|select(.reachable==true and (.url|startswith("http"))))] | length')
  if [ "$enabled" = "true" ] && [ "$live" = "true" ] && [ "\${linked:-0}" -ge 1 ]; then
    printf '%s' "\${GREEN}● mesh\${RST}"
  elif [ "$enabled" = "true" ]; then
    printf '%s' "\${RED}● mesh…\${RST}"
  else
    printf '%s' "\${YELLOW}○ local\${RST}"
  fi
}`

/** Render block for the bus dot (color defs + the BUS_SEG assignment). */
export const BUS_DOT_RENDER = `${COLOR_DEFS}
if bus_watcher_armed "$SESSION_ID"; then
  BUS_SEG="\${GREEN}● bus\${RST}"
else
  BUS_SEG="\${RED}● bus off\${RST}"
fi`

/** Render block for the mesh dot — the function prints the colored segment itself. */
export const MESH_DOT_RENDER = `MESH_SEG=$(mesh_seg)`

/**
 * Compose a MINIMAL, Cortex-dots-only statusline script for the chosen dots
 * (subset of 'bus' / 'mesh'). This is the public installer scaffold: just the
 * Cortex indicators — deliberately NOT the rich personal statusline (no model
 * line, context bar, rate-limits, or bc dependency). A user who wants more keeps
 * their own script and pastes the snippet instead. Cheap-per-render by
 * construction (proc scan + one bounded curl; no spawn).
 */
export const composeScaffold = (dots: string[]): string => {
  const has = (d: string): boolean => dots.includes(d)
  const out: string[] = [
    '#!/bin/bash',
    '# Cortex statusline — minimal Cortex dots only (generated by ckn-statusline).',
    '# Cortex ships none by default; this exists because you opted in. Edit freely.',
    'input=$(cat)',
  ]
  if (has('bus')) out.push(`SESSION_ID=$(echo "$input" | jq -r '.session_id // empty')`)
  out.push(COLOR_DEFS)
  if (has('bus')) out.push(BUS_DOT_FN)
  if (has('mesh')) out.push(MESH_DOT_FN)
  if (has('bus')) {
    out.push(
      `if bus_watcher_armed "$SESSION_ID"; then BUS_SEG="\${GREEN}● bus\${RST}"; else BUS_SEG="\${RED}● bus off\${RST}"; fi`,
    )
  }
  if (has('mesh')) out.push('MESH_SEG=$(mesh_seg)')
  const segs = [has('bus') ? '${BUS_SEG}' : null, has('mesh') ? '${MESH_SEG}' : null]
    .filter(Boolean)
    .join('  ')
  out.push(`printf '%b\\n' "${segs}"`)
  return out.join('\n') + '\n'
}
